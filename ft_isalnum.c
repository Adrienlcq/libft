#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}

int main(int ac, char **av)
{
	if (!ft_isalnum(av[1][0]))
		return (1);
	return (0);
}
