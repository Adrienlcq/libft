#include "libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int main(int ac, char **av)
{
	if (!ft_isdigit(av[1][0]))
		return (1);
	return (0);
}
